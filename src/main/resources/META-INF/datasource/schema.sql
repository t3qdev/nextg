CREATE TABLE IF NOT EXISTS ARTICLE
(
IDX INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) NOT NULL,
TITLE VARCHAR(100),
CONTENT VARCHAR(500),
WRITER VARCHAR(100),
STATE VARCHAR(100),
REGDATE DATETIME DEFAULT CURRENT_TIMESTAMP,
PRIMARY KEY (IDX)
);

